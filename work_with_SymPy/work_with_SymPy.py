''''–ß—Ç–æ —Ç–∞–∫–æ–µ SymPy? –≠—Ç–æ –±–∏–±–ª–∏–æ—Ç–µ–∫–∞ —Å–∏–º–≤–æ–ª—å–Ω–æ–π –º–∞—Ç–µ–º–∞—Ç–∏–∫–∏ —è–∑—ã–∫–∞ Python. –û–Ω–∞ —è–≤–ª—è–µ—Ç—Å—è —Ä–µ–∞–ª—å–Ω–æ–π –∞–ª—å—Ç–µ—Ä–Ω–∞—Ç–∏–≤–æ–π —Ç–∞–∫–∏–º 
–º–∞—Ç–µ–º–∞—Ç–∏—á–µ—Å–∫–∏–º –ø–∞–∫–µ—Ç–∞–º –∫–∞–∫ Mathematica –∏–ª–∏ Maple –∏ –æ–±–ª–∞–¥–∞–µ—Ç –æ—á–µ–Ω—å –ø—Ä–æ—Å—Ç—ã–º –∏ –ª–µ–≥–∫–æ —Ä–∞—Å—à–∏—Ä—è–µ–º—ã–º –∫–æ–¥–æ–º. SymPy –Ω–∞–ø–∏—Å–∞–Ω–∞ 
–∏—Å–∫–ª—é—á–∏—Ç–µ–ª—å–Ω–æ –Ω–∞ —è–∑—ã–∫–µ Python –∏ –Ω–µ —Ç—Ä–µ–±—É–µ—Ç –Ω–∏–∫–∞–∫–∏—Ö —Å—Ç–æ—Ä–æ–Ω–Ω–∏—Ö –±–∏–±–ª–∏–æ—Ç–µ–∫.
–î–æ–∫—É–º–µ–Ω—Ç–∞—Ü–∏—é –∏ –∏—Å—Ö–æ–¥–Ω—ã–π –∫–æ–¥ —ç—Ç–æ–π –±–∏–±–ª–∏–æ—Ç–µ–∫–∏ –º–æ–∂–Ω–æ –Ω–∞–π—Ç–∏ –Ω–∞ –µ–µ –æ—Ñ–∏—Ü–∏–∞–ª—å–Ω–æ–π —Å—Ç—Ä–∞–Ω–∏—Ü–µ.

–ò—Å–ø–æ–ª—å–∑—É–µ–º SymPy –∫–∞–∫ –æ–±—ã—á–Ω—ã–π –∫–∞–ª—å–∫—É–ª—è—Ç–æ—Ä
–í –±–∏–±–ª–∏–æ—Ç–µ–∫–µ SymPy –µ—Å—Ç—å —Ç—Ä–∏ –≤—Å—Ç—Ä–æ–µ–Ω–Ω—ã—Ö —á–∏—Å–ª–µ–Ω–Ω—ã—Ö —Ç–∏–ø–∞ –¥–∞–Ω–Ω—ã—Ö: Real, Rational –∏ Integer. –° Real –∏ Integer –≤—Å–µ –ø–æ–Ω—è—Ç–Ω–æ, 
–∞ –∫–ª–∞—Å—Å Rational –ø—Ä–µ–¥—Å—Ç–∞–≤–ª—è–µ—Ç —Ä–∞—Ü–∏–æ–Ω–∞–ª—å–Ω–æ–µ —á–∏—Å–ª–æ –∫–∞–∫ –ø–∞—Ä—É —á–∏—Å–µ–ª: —á–∏—Å–ª–∏—Ç–µ–ª—å –∏ –∑–Ω–∞–º–µ–Ω–∞—Ç–µ–ª—å —Ä–∞—Ü–∏–æ–Ω–∞–ª—å–Ω–æ–π –¥—Ä–æ–±–∏. 
–¢–∞–∫–∏–º –æ–±—Ä–∞–∑–æ–º, Rational(1, 2) –ø—Ä–µ–¥—Å—Ç–∞–≤–ª—è–µ—Ç —Å–æ–±–æ–π 1/2, –∞, –Ω–∞–ø—Ä–∏–º–µ—Ä, Rational(5, 2) ‚Äî —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤–µ–Ω–Ω–æ 5/2.
'''''
import sympy as sym
a = sym.Rational(1, 2) # –¥–∞–µ—Ç 1/2
a * 2 # –¥–∞–µ—Ç 1

''''–ë–∏–±–ª–∏–æ—Ç–µ–∫–∞ SymPy –∏—Å–ø–æ–ª—å–∑—É–µ—Ç –±–∏–±–ª–∏–æ—Ç–µ–∫—É mpmath, —á—Ç–æ –ø–æ–∑–≤–æ–ª—è–µ—Ç –ø—Ä–æ–∏–∑–≤–æ–¥–∏—Ç—å –≤—ã—á–∏—Å–ª–µ–Ω–∏—è —Å –ø—Ä–æ–∏–∑–≤–æ–ª—å–Ω–æ–π —Ç–æ—á–Ω–æ—Å—Ç—å—é. –¢–∞–∫–∏–º 
–æ–±—Ä–∞–∑–æ–º, —Ä—è–¥ –∫–æ–Ω—Å—Ç–∞–Ω—Ç (–Ω–∞–ø—Ä–∏–º–µ—Ä, –ø–∏, e), –∫–æ—Ç–æ—Ä—ã–µ –≤ –¥–∞–Ω–Ω–æ–π –±–∏–±–ª–∏–æ—Ç–µ–∫–µ —Ä–∞—Å—Å–º–∞—Ç—Ä–∏–≤–∞—é—Ç—Å—è –∫–∞–∫ —Å–∏–º–≤–æ–ª—ã, –º–æ–≥—É—Ç –±—ã—Ç—å –≤—ã—á–∏—Å–ª–µ–Ω—ã 
—Å –ª—é–±–æ–π —Ç–æ—á–Ω–æ—Å—Ç—å—é.'''''
sym.pi**2 # —Ä–µ–∑—É–ª—å—Ç–∞—Ç pi**2
sym.pi.evalf() # —Ä–µ–∑—É–ª—å—Ç–∞—Ç 3.14159265358979
(sym.pi + sym.exp(1)).evalf() # —Ä–µ–∑—É–ª—å—Ç–∞—Ç 5.85987448204884
''''–ö–∞–∫ –º–æ–∂–Ω–æ –∑–∞–º–µ—Ç–∏—Ç—å, —Ñ—É–Ω–∫—Ü–∏—è evalf() –¥–∞–µ—Ç –Ω–∞ –≤—ã—Ö–æ–¥–µ —á–∏—Å–ª–æ —Å –ø–ª–∞–≤–∞—é—â–µ–π —Ç–æ—á–∫–æ–π.

–í SymPy –µ—Å—Ç—å —Ç–∞–∫–∂–µ –∫–ª–∞—Å—Å, –ø—Ä–µ–¥—Å—Ç–∞–≤–ª—è—é—â–∏–π —Ç–∞–∫–æ–µ –ø–æ–Ω—è—Ç–∏–µ –≤ –º–∞—Ç–µ–º–∞—Ç–∏–∫–µ, –∫–∞–∫ –±–µ—Å–∫–æ–Ω–µ—á–Ω–æ—Å—Ç—å. –û–Ω –æ–±–æ–∑–Ω–∞—á–∞–µ—Ç—Å—è —Å–ª–µ–¥—É—é—â–∏–º 
–æ–±—Ä–∞–∑–æ–º: oo.'''''
sym.oo > 99999 # —Ä–µ–∑—É–ª—å—Ç–∞—Ç True
sym.oo + 1 # —Ä–µ–∑—É–ª—å—Ç–∞—Ç oo

''''–°–∏–º–≤–æ–ª—ã
–í –æ—Ç–ª–∏—á–∏–µ –æ—Ç —Ä—è–¥–∞ –¥—Ä—É–≥–∏—Ö —Å–∏—Å—Ç–µ–º –∫–æ–º–ø—å—é—Ç–µ—Ä–Ω–æ–π –∞–ª–≥–µ–±—Ä—ã, –≤ SymPy –º–æ–∂–Ω–æ –≤ —è–≤–Ω–æ–º –≤–∏–¥–µ –∑–∞–¥–∞–≤–∞—Ç—å —Å–∏–º–≤–æ–ª—å–Ω—ã–µ –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ. –≠—Ç–æ 
–ø—Ä–æ–∏—Å—Ö–æ–¥–∏—Ç —Å–ª–µ–¥—É—é—â–∏–º –æ–±—Ä–∞–∑–æ–º:'''''
x = sym.Symbol('x')
y = sym.Symbol('y')

#–ü–æ—Å–ª–µ –∏—Ö –∑–∞–¥–∞–Ω–∏—è, —Å –Ω–∏–º–∏ –º–æ–∂–Ω–æ –ø—Ä–æ–∏–∑–≤–æ–¥–∏—Ç—å —Ä–∞–∑–ª–∏—á–Ω—ã–µ –º–∞–Ω–∏–ø—É–ª—è—Ü–∏–∏.
x + y + x - y # —Ä–µ–∑—É–ª—å—Ç–∞—Ç 2*x
(x + y) ** 2 # —Ä–µ–∑—É–ª—å—Ç–∞—Ç (x + y)**2

''''–° —Å–∏–º–≤–æ–ª–∞–º–∏ –º–æ–∂–Ω–æ –ø—Ä–æ–∏–∑–≤–æ–¥–∏—Ç—å –ø—Ä–µ–æ–±—Ä–∞–∑–æ–≤–∞–Ω–∏—è —Å –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ–º –Ω–µ–∫–æ—Ç–æ—Ä—ã—Ö –æ–ø–µ—Ä–∞—Ç–æ—Ä–æ–≤ —è–∑—ã–∫–∞ Python. –ê –∏–º–µ–Ω–Ω–æ, 
–∞—Ä–∏—Ñ–º–µ—Ç–∏—á–µ—Å–∫–∏—Ö (+, -`, ``*, **) –∏ –ª–æ–≥–∏—á–µ—Å–∫–∏—Ö (&, |, ~) .
–ë–∏–±–ª–∏–æ—Ç–µ–∫–∞ SymPy –ø–æ–∑–≤–æ–ª—è–µ—Ç –∑–∞–¥–∞–≤–∞—Ç—å —Ñ–æ—Ä–º—É –≤—ã–≤–æ–¥–∞ —Ä–µ–∑—É–ª—å—Ç–∞—Ç–æ–≤ –Ω–∞ —ç–∫—Ä–∞–Ω. –û–±—ã—á–Ω–æ –º—ã –∏—Å–ø–æ–ª—å–∑—É–µ–º —Ñ–æ—Ä–º–∞—Ç —Ç–∞–∫–æ–≥–æ –≤–∏–¥–∞:'''''
sym.init_printing(use_unicode=False, wrap_line=True)

''''–ê–ª–≥–µ–±—Ä–∞–∏—á–µ—Å–∫–∏–µ –ø—Ä–µ–æ–±—Ä–∞–∑–æ–≤–∞–Ω–∏—è
SymPy —Å–ø–æ—Å–æ–±–Ω–∞ –Ω–∞ —Å–ª–æ–∂–Ω—ã–µ –∞–ª–≥–µ–±—Ä–∞–∏—á–µ—Å–∫–∏–µ –ø—Ä–µ–æ–±—Ä–∞–∑–æ–≤–∞–Ω–∏—è. –ó–¥–µ—Å—å –º—ã —Ä–∞—Å—Å–º–æ—Ç—Ä–∏–º –Ω–∞–∏–±–æ–ª–µ–µ –≤–æ—Å—Ç—Ä–µ–±–æ–≤–∞–Ω–Ω—ã–µ –∏–∑ –Ω–∏—Ö, –∞ –∏–º–µ–Ω–Ω–æ 
—Ä–∞—Å–∫—Ä—ã—Ç–∏–µ —Å–∫–æ–±–æ–∫ –∏ —É–ø—Ä–æ—â–µ–Ω–∏–µ –≤—ã—Ä–∞–∂–µ–Ω–∏–π.

–†–∞—Å–∫—Ä—ã—Ç–∏–µ —Å–∫–æ–±–æ–∫
–ß—Ç–æ–±—ã —Ä–∞—Å–∫—Ä—ã—Ç—å —Å–∫–æ–±–∫–∏ –≤ –∞–ª–≥–µ–±—Ä–∞–∏—á–µ—Å–∫–∏—Ö –≤—ã—Ä–∞–∂–µ–Ω–∏—è—Ö, –∏—Å–ø–æ–ª—å–∑—É–π—Ç–µ —Å–ª–µ–¥—É—é—â–∏–π —Å–∏–Ω—Ç–∞–∫—Å–∏—Å:'''''

sym.expand((x + y) ** 3)

'''ùë•3+3ùë•2ùë¶+3ùë•ùë¶2+ùë¶3
3 * x * y ** 2 + 3 * y * x ** 2 + x ** 3 + y ** 3
ùë•3+3ùë•2ùë¶+3ùë•ùë¶2+ùë¶3
–ü—Ä–∏ –ø–æ–º–æ—â–∏ –∫–ª—é—á–µ–≤–æ–≥–æ —Å–ª–æ–≤–∞ –º–æ–∂–Ω–æ –¥–æ–±–∞–≤–∏—Ç—å –ø–æ–¥–¥–µ—Ä–∂–∫—É —Ä–∞–±–æ—Ç—ã —Å –∫–æ–º–ø–ª–µ–∫—Å–Ω—ã–º–∏ –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–º–∏, –∞ —Ç–∞–∫–∂–µ —Ä–∞—Å–∫—Ä—ã—Ç–∏–µ —Å–∫–æ–±–æ–∫ –≤ 
—Ç—Ä–∏–≥–æ–Ω–æ–º–µ—Ç—Ä–∏—á–µ—Å–∫–∏—Ö —Ñ—É–Ω–∫—Ü–∏—è—Ö.'''''
sym.expand(x + y, complex=True)#—Ä–µ–∑—É–ª—å—Ç–∞—Ç re(ùë•)+re(ùë¶)+ùëñim(ùë•)+ùëñim(ùë¶)
sym.expand(sym.cos(x + y), trig=True)
# —Ä–µ–∑—É–ª—å—Ç–∞—Ç ‚àísin(ùë•)sin(ùë¶)+cos(ùë•)cos(ùë¶)

''''–£–ø—Ä–æ—â–µ–Ω–∏–µ –≤—ã—Ä–∞–∂–µ–Ω–∏–π
–ï—Å–ª–∏ –≤—ã —Ö–æ—Ç–∏—Ç–µ –ø—Ä–∏–≤–µ—Å—Ç–∏ –≤—ã—Ä–∞–∂–µ–Ω–∏–µ –∫ –±–æ–ª–µ–µ –ø—Ä–æ—Å—Ç–æ–º—É –≤–∏–¥—É (–≤–æ–∑–º–æ–∂–Ω–æ, —Å–æ–∫—Ä–∞—Ç–∏—Ç—å –∫–∞–∫–∏–µ-—Ç–æ —á–ª–µ–Ω—ã), —Ç–æ –∏—Å–ø–æ–ª—å–∑—É–π—Ç–µ —Ñ—É–Ω–∫—Ü–∏—é simplify.'''''
sym.simplify((x + x * y) / x) # —Ä–µ–∑—É–ª—å—Ç–∞—Ç y + 1

''''–¢–∞–∫–∂–µ –Ω–∞–¥–æ —Å–∫–∞–∑–∞—Ç—å, —á—Ç–æ –¥–ª—è –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–Ω—ã—Ö –≤–∏–¥–æ–≤ –º–∞—Ç–µ–º–∞—Ç–∏—á–µ—Å–∫–∏—Ö —Ñ—É–Ω–∫—Ü–∏–π —Å—É—â–µ—Å—Ç–≤—É—é—Ç –∞–ª—å—Ç–µ—Ä–Ω–∞—Ç–∏–≤–Ω—ã–µ, –±–æ–ª–µ–µ –∫–æ–Ω–∫—Ä–µ—Ç–Ω—ã–µ 
—Ñ—É–Ω–∫—Ü–∏–∏ –¥–ª—è —É–ø—Ä–æ—â–µ–Ω–∏—è –≤—ã—Ä–∞–∂–µ–Ω–∏–π. –¢–∞–∫, –¥–ª—è —É–ø—Ä–æ—â–µ–Ω–∏—è —Å—Ç–µ–ø–µ–Ω–Ω—ã—Ö —Ñ—É–Ω–∫—Ü–∏–π –µ—Å—Ç—å —Ñ—É–Ω–∫—Ü–∏—è powsimp, –¥–ª—è —Ç—Ä–∏–≥–æ–Ω–æ–º–µ—Ç—Ä–∏—á–µ—Å–∫–∏—Ö 
‚Äîtrigsimp, –∞ –¥–ª—è –ª–æ–≥–∞—Ä–∏—Ñ–º–∏—á–µ—Å–∫–∏—Ö ‚Äî logcombine, radsimp.

–í—ã—á–∏—Å–ª–µ–Ω–∏—è
–í—ã—á–∏—Å–ª–µ–Ω–∏—è –ø—Ä–µ–¥–µ–ª–æ–≤
–î–ª—è –≤—ã—á–∏—Å–ª–µ–Ω–∏—è –ø—Ä–µ–¥–µ–ª–æ–≤ –≤ SymPy –ø—Ä–µ–¥—É—Å–º–æ—Ç—Ä–µ–Ω –æ—á–µ–Ω—å –ø—Ä–æ—Å—Ç–æ–π —Å–∏–Ω—Ç–∞–∫—Å–∏—Å, –∞ –∏–º–µ–Ω–Ω–æ limit(function, variable, point). 
–ù–∞–ø—Ä–∏–º–µ—Ä, –µ—Å–ª–∏ –≤—ã —Ö–æ—Ç–∏—Ç–µ –≤—ã—á–∏—Å–ª–∏—Ç—å –ø—Ä–µ–¥–µ–ª —Ñ—É–Ω–∫—Ü–∏–∏ f(x), –≥–¥–µ x -> 0, —Ç–æ –Ω–∞–¥–æ –Ω–∞–ø–∏—Å–∞—Ç—å limit(f(x), x, 0).'''''
sym.limit(sym.sin(x) / x, x, 0) # —Ä–µ–∑—É–ª—å—Ç–∞—Ç 1

#–¢–∞–∫–∂–µ –º–æ–∂–Ω–æ –≤—ã—á–∏—Å–ª—è—Ç—å –ø—Ä–µ–¥–µ–ª—ã, –∫–æ—Ç–æ—Ä—ã–µ —Å—Ç—Ä–µ–º—è—Ç—Å—è –∫ –±–µ—Å–∫–æ–Ω–µ—á–Ω–æ—Å—Ç–∏.
sym.limit(x, x, sym.oo) # —Ä–µ–∑—É–ª—å—Ç–∞—Ç oo
sym.limit(1 / x, x, sym.oo) # —Ä–µ–∑—É–ª—å—Ç–∞—Ç 0
sym.limit(x ** x, x, 0) # —Ä–µ–∑—É–ª—å—Ç–∞—Ç 1

''''–î–∏—Ñ—Ñ–µ—Ä–µ–Ω—Ü–∏—Ä–æ–≤–∞–Ω–∏–µ
–î–ª—è –¥–∏—Ñ—Ñ–µ—Ä–µ–Ω—Ü–∏—Ä–æ–≤–∞–Ω–∏—è –≤—ã—Ä–∞–∂–µ–Ω–∏–π –≤ SymPy –µ—Å—Ç—å —Ñ—É–Ω–∫—Ü–∏—è diff(func, var). –ù–∏–∂–µ –¥–∞–Ω—ã –ø—Ä–∏–º–µ—Ä—ã –µ–µ —Ä–∞–±–æ—Ç—ã.'''''
sym.diff(sym.sin(x), x) # —Ä–µ–∑—É–ª—å—Ç–∞—Ç cos(ùë•)
sym.diff(sym.sin(2 * x), x) # —Ä–µ–∑—É–ª—å—Ç–∞—Ç 2cos(2ùë•)
sym.diff(sym.tan(x), x)

''''tan2(ùë•)+1
–ü—Ä–æ–≤–µ—Ä–∏–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç –ø–æ—Å–ª–µ–¥–Ω–µ–π —Ñ—É–Ω–∫—Ü–∏–∏ –ø—Ä–∏ –ø–æ–º–æ—â–∏ –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–∏—è –ø—Ä–æ–∏–∑–≤–æ–¥–Ω–æ–π —á–µ—Ä–µ–∑ –ø—Ä–µ–¥–µ–ª.'''''
sym.limit((sym.tan(x + y) - sym.tan(x)) / y, y, 0)

''''tan2(ùë•)+1 –†–µ–∑—É–ª—å—Ç–∞—Ç —Ç–æ—Ç –∂–µ.
–¢–∞–∫–∂–µ –ø—Ä–∏ –ø–æ–º–æ—â–∏ —ç—Ç–æ–π –∂–µ —Ñ—É–Ω–∫—Ü–∏–∏ –º–æ–≥—É—Ç –±—ã—Ç—å –≤—ã—á–∏—Å–ª–µ–Ω—ã –ø—Ä–æ–∏–∑–≤–æ–¥–Ω—ã–µ –±–æ–ª–µ–µ –≤—ã—Å–æ–∫–∏—Ö –ø–æ—Ä—è–¥–∫–æ–≤. –°–∏–Ω—Ç–∞–∫—Å–∏—Å —Ñ—É–Ω–∫—Ü–∏–∏ –±—É–¥–µ—Ç 
—Å–ª–µ–¥—É—é—â–∏–º: diff(func, var, n). –ù–∏–∂–µ –ø—Ä–∏–≤–µ–¥–µ–Ω–æ –Ω–µ—Å–∫–æ–ª—å–∫–æ –ø—Ä–∏–º–µ—Ä–æ–≤.'''''
sym.diff(sym.sin(2 * x), x, 1) # —Ä–µ–∑—É–ª—å—Ç–∞—Ç 2cos(2ùë•)
sym.diff(sym.sin(2 * x), x, 2) # —Ä–µ–∑—É–ª—å—Ç–∞—Ç ‚àí4sin(2ùë•)
sym.diff(sym.sin(2 * x), x, 3) # —Ä–µ–∑—É–ª—å—Ç–∞—Ç ‚àí8cos(2ùë•)

''''–†–∞–∑–ª–æ–∂–µ–Ω–∏–µ –≤ —Ä—è–¥
–î–ª—è —Ä–∞–∑–ª–æ–∂–µ–Ω–∏—è –≤—ã—Ä–∞–∂–µ–Ω–∏—è –≤ —Ä—è–¥ –¢–µ–π–ª–æ—Ä–∞ –∏—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è —Å–ª–µ–¥—É—é—â–∏–π —Å–∏–Ω—Ç–∞–∫—Å–∏—Å: series(expr, var).'''''
sym.series(sym.cos(x), x)
#1‚àíùë•2/2+ùë•4/24+ùëÇ(ùë•6)

sym.series(1/sym.cos(x), x)
#1+ùë•2/2+5ùë•4/24+ùëÇ(ùë•6)

''''–ò–Ω—Ç–µ–≥—Ä–∏—Ä–æ–≤–∞–Ω–∏–µ
–í SymPy —Ä–µ–∞–ª–∏–∑–æ–≤–∞–Ω–∞ –ø–æ–¥–¥–µ—Ä–∂–∫–∞ –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–Ω—ã—Ö –∏ –Ω–µ–æ–ø—Ä–µ–¥–µ–ª–µ–Ω–Ω—ã—Ö –∏–Ω—Ç–µ–≥—Ä–∞–ª–æ–≤ –ø—Ä–∏ –ø–æ–º–æ—â–∏ —Ñ—É–Ω–∫—Ü–∏–∏ integrate(). –ò–Ω—Ç–µ–≥—Ä–∏—Ä–æ–≤–∞—Ç—å –º–æ–∂–Ω–æ 
—ç–ª–µ–º–µ–Ω—Ç–∞—Ä–Ω—ã–µ, —Ç—Ä–∞–Ω—Å—Ü–µ–Ω–¥–µ–Ω—Ç–Ω—ã–µ –∏ —Å–ø–µ—Ü–∏–∞–ª—å–Ω—ã–µ —Ñ—É–Ω–∫—Ü–∏–∏. –ò–Ω—Ç–µ–≥—Ä–∏—Ä–æ–≤–∞–Ω–∏–µ –æ—Å—É—â–µ—Å—Ç–≤–ª—è–µ—Ç—Å—è —Å –ø–æ–º–æ—â—å—é —Ä–∞—Å—à–∏—Ä–µ–Ω–Ω–æ–≥–æ –∞–ª–≥–æ—Ä–∏—Ç–º–∞ 
–†–∏—à–∞-–ù–æ—Ä–º–∞–Ω–∞. –¢–∞–∫–∂–µ –∏—Å–ø–æ–ª—å–∑—É—é—Ç—Å—è —Ä–∞–∑–ª–∏—á–Ω—ã–µ —ç–≤—Ä–∏—Å—Ç–∏–∫–∏ –∏ —à–∞–±–ª–æ–Ω—ã. –í–æ—Ç –ø—Ä–∏–º–µ—Ä—ã –∏–Ω—Ç–µ–≥—Ä–∏—Ä–æ–≤–∞–Ω–∏—è —ç–ª–µ–º–µ–Ω—Ç–∞—Ä–Ω—ã—Ö —Ñ—É–Ω–∫—Ü–∏–π:'''''
sym.integrate(sym.sin(x), x) # —Ä–µ–∑—É–ª—å—Ç–∞—Ç ‚àícos(ùë•)
sym.integrate(sym.log(x), x) # —Ä–µ–∑—É–ª—å—Ç–∞—Ç ùë•log(ùë•)‚àíùë•

#–¢–∞–∫–∂–µ –Ω–µ—Å–ª–æ–∂–Ω–æ –ø–æ—Å—á–∏—Ç–∞—Ç—å –∏–Ω—Ç–µ–≥—Ä–∞–ª –∏ –æ—Ç —Å–ø–µ—Ü–∏–∞–ª—å–Ω—ã—Ö —Ñ—É–Ω–∫—Ü–∏–π. –í–æ–∑—å–º–µ–º, –Ω–∞–ø—Ä–∏–º–µ—Ä, —Ñ—É–Ω–∫—Ü–∏—é –ì–∞—É—Å—Å–∞:
sym.integrate(sym.exp(-x ** 2) * sym.erf(x), x)

#–†–µ–∑—É–ª—å—Ç–∞—Ç –≤—ã—á–∏—Å–ª–µ–Ω–∏—è –º–æ–∂–µ—Ç–µ –ø–æ—Å–º–æ—Ç—Ä–µ—Ç—å —Å–∞–º–∏. –í–æ—Ç –ø—Ä–∏–º–µ—Ä—ã –≤—ã—á–∏—Å–ª–µ–Ω–∏—è –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–Ω—ã—Ö –∏–Ω—Ç–µ–≥—Ä–∞–ª–æ–≤.
sym.integrate(x**3, (x, -1, 1)) # —Ä–µ–∑—É–ª—å—Ç–∞—Ç 0
sym.integrate(sym.sin(x), (x, 0, sym.pi / 2)) # —Ä–µ–∑—É–ª—å—Ç–∞—Ç 1
sym.integrate(sym.cos(x), (x, -sym.pi / 2, sym.pi / 2))
# —Ä–µ–∑—É–ª—å—Ç–∞—Ç 2
#–¢–∞–∫–∂–µ –º–æ–∂–Ω–æ –≤—ã—á–∏—Å–ª—è—Ç—å –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–Ω—ã–µ –∏–Ω—Ç–µ–≥—Ä–∞–ª—ã —Å –±–µ—Å–∫–æ–Ω–µ—á–Ω—ã–º–∏ –ø—Ä–µ–¥–µ–ª–∞–º–∏ –∏–Ω—Ç–µ–≥—Ä–∏—Ä–æ–≤–∞–Ω–∏—è (–Ω–µ—Å–æ–±—Å—Ç–≤–µ–Ω–Ω—ã–µ –∏–Ω—Ç–µ–≥—Ä–∞–ª—ã).
sym.integrate(sym.exp(-x), (x, 0, sym.oo)) # —Ä–µ–∑—É–ª—å—Ç–∞—Ç 1

''''–†–µ—à–µ–Ω–∏–µ —É—Ä–∞–≤–Ω–µ–Ω–∏–π
–ü—Ä–∏ –ø–æ–º–æ—â–∏ SymPy –º–æ–∂–Ω–æ —Ä–µ—à–∞—Ç—å –∞–ª–≥–µ–±—Ä–∞–∏—á–µ—Å–∫–∏–µ —É—Ä–∞–≤–Ω–µ–Ω–∏—è —Å –æ–¥–Ω–æ–π –∏–ª–∏ –Ω–µ—Å–∫–æ–ª—å–∫–∏–º–∏ –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–º–∏. –î–ª—è —ç—Ç–æ–≥–æ –∏—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è —Ñ—É–Ω–∫—Ü–∏—è solveset().'''''
sym.solveset(x ** 4 - 1, x) # —Ä–µ–∑—É–ª—å—Ç–∞—Ç {‚àí1,1,‚àíùëñ,ùëñ}

''''–ö–∞–∫ –º–æ–∂–Ω–æ –∑–∞–º–µ—Ç–∏—Ç—å, –ø–µ—Ä–≤–æ–µ –≤—ã—Ä–∞–∂–µ–Ω–∏–µ —Ñ—É–Ω–∫—Ü–∏–∏ solveset() –ø—Ä–∏—Ä–∞–≤–Ω–∏–≤–∞–µ—Ç—Å—è –∫ 0 –∏ —Ä–µ—à–∞–µ—Ç—Å—è –æ—Ç–Ω–æ—Å–∏—Ç–µ–ª—å–Ω–æ —Ö. –¢–∞–∫–∂–µ –≤–æ–∑–º–æ–∂–Ω–æ 
—Ä–µ—à–∞—Ç—å –Ω–µ–∫–æ—Ç–æ—Ä—ã–µ —É—Ä–∞–≤–Ω–µ–Ω–∏—è —Å —Ç—Ä–∞–Ω—Å—Ü–µ–Ω–¥–µ–Ω—Ç–Ω—ã–º–∏ —Ñ—É–Ω–∫—Ü–∏—è–º–∏.'''''
sym.solveset(sym.exp(x) + 1, x) # —Ä–µ–∑—É–ª—å—Ç–∞—Ç {ùëñ(2ùëõùúã+ùúã)|ùëõ‚àà‚Ñ§}

''''–°–∏—Å—Ç–µ–º—ã –ª–∏–Ω–µ–π–Ω—ã—Ö —É—Ä–∞–≤–Ω–µ–Ω–∏–π
SymPy —Å–ø–æ—Å–æ–±–Ω–∞ —Ä–µ—à–∞—Ç—å —à–∏—Ä–æ–∫–∏–π –∫–ª–∞—Å—Å –ø–æ–ª–∏–Ω–æ–º–∏–∞–ª—å–Ω—ã—Ö —É—Ä–∞–≤–Ω–µ–Ω–∏–π. –¢–∞–∫–∂–µ –ø—Ä–∏ –ø–æ–º–æ—â–∏ –¥–∞–Ω–Ω–æ–π –±–∏–±–ª–∏–æ—Ç–µ–∫–∏ –º–æ–∂–Ω–æ —Ä–µ—à–∞—Ç—å –∏ —Å–∏—Å—Ç–µ–º—ã 
—É—Ä–∞–≤–Ω–µ–Ω–∏–π. –ü—Ä–∏ —ç—Ç–æ–º –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ, –æ—Ç–Ω–æ—Å–∏—Ç–µ–ª—å–Ω–æ –∫–æ—Ç–æ—Ä—ã—Ö –¥–æ–ª–∂–Ω–∞ –±—ã—Ç—å —Ä–∞–∑—Ä–µ—à–µ–Ω–∞ —Å–∏—Å—Ç–µ–º–∞, –ø–µ—Ä–µ–¥–∞—é—Ç—Å—è –≤ –≤–∏–¥–µ –∫–æ—Ä—Ç–µ–∂–∞ –≤–æ –≤—Ç–æ—Ä–æ–º 
–∞—Ä–≥—É–º–µ–Ω—Ç–µ —Ñ—É–Ω–∫—Ü–∏–∏ solve(), –∫–æ—Ç–æ—Ä–∞—è –∏—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è –¥–ª—è —Ç–∞–∫–∏—Ö –∑–∞–¥–∞—á.'''''
solution = sym.solve((x + 5 * y - 2, -3 * x + 6 * y - 15), (x, y))
solution[x], solution[y] # —Ä–µ–∑—É–ª—å—Ç–∞—Ç (-3, 1)

''''–§–∞–∫—Ç–æ—Ä–∏–∑–∞—Ü–∏—è
–î—Ä—É–≥–∏–º –º–æ—â–Ω—ã–º –º–µ—Ç–æ–¥–æ–º –∏—Å—Å–ª–µ–¥–æ–≤–∞–Ω–∏—è –ø–æ–ª–∏–Ω–æ–º–∏–∞–ª—å–Ω—ã—Ö —É—Ä–∞–≤–Ω–µ–Ω–∏–π —è–≤–ª—è–µ—Ç—Å—è —Ñ–∞–∫—Ç–æ—Ä–∏–∑–∞—Ü–∏—è –º–Ω–æ–≥–æ—á–ª–µ–Ω–æ–≤ (—Ç–æ –µ—Å—Ç—å –ø—Ä–µ–¥—Å—Ç–∞–≤–ª–µ–Ω–∏–µ 
–º–Ω–æ–≥–æ—á–ª–µ–Ω–∞ –≤ –≤–∏–¥–µ –ø—Ä–æ–∏–∑–≤–µ–¥–µ–Ω–∏—è –º–Ω–æ–≥–æ—á–ª–µ–Ω–æ–≤ –º–µ–Ω—å—à–∏—Ö —Å—Ç–µ–ø–µ–Ω–µ–π). –î–ª—è —ç—Ç–æ–≥–æ –≤ SymPy –ø—Ä–µ–¥—É—Å–º–æ—Ç—Ä–µ–Ω–∞ —Ñ—É–Ω–∫—Ü–∏—è factor(), –∫–æ—Ç–æ—Ä–∞—è 
—Å–ø–æ—Å–æ–±–Ω–∞ –ø—Ä–æ–∏–∑–≤–æ–¥–∏—Ç—å —Ñ–∞–∫—Ç–æ—Ä–∏–∑–∞—Ü–∏—é –æ—á–µ–Ω—å —à–∏—Ä–æ–∫–æ–≥–æ –∫–ª–∞—Å—Å–∞ –ø–æ–ª–∏–Ω–æ–º–æ–≤.'''''
f = x ** 4 - 3 * x ** 2 + 1
sym.factor(f)
#(ùë•2‚àíùë•‚àí1)(ùë•2+ùë•‚àí1)
sym.factor(f, modulus=5)
#(ùë•‚àí2)2(ùë•+2)2

''''–ë—É–ª–µ–≤—ã —É—Ä–∞–≤–Ω–µ–Ω–∏—è
–¢–∞–∫–∂–µ –≤ SymPy —Ä–µ–∞–ª–∏–∑–æ–≤–∞–Ω–∞ –≤–æ–∑–º–æ–∂–Ω–æ—Å—Ç—å —Ä–µ—à–µ–Ω–∏—è –±—É–ª–µ–≤—ã—Ö —É—Ä–∞–≤–Ω–µ–Ω–∏–π, —á—Ç–æ –ø–æ —Å—É—Ç–∏ –æ–∑–Ω–∞—á–∞–µ—Ç –ø—Ä–æ–≤–µ—Ä–∫—É –±—É–ª–µ–≤–æ–≥–æ –≤—ã—Ä–∞–∂–µ–Ω–∏—è –Ω–∞ 
–∏—Å—Ç–∏–Ω–Ω–æ—Å—Ç—å. –î–ª—è —ç—Ç–æ–≥–æ –∏—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è —Ñ—É–Ω–∫—Ü–∏—è satisfiable().'''''
sym.satisfiable(x & y) # —Ä–µ–∑—É–ª—å—Ç–∞—Ç {x: True, y: True}

''''–î–∞–Ω–Ω—ã–π —Ä–µ–∑—É–ª—å—Ç–∞—Ç –≥–æ–≤–æ—Ä–∏—Ç –Ω–∞–º –æ —Ç–æ–º, —á—Ç–æ –≤—ã—Ä–∞–∂–µ–Ω–∏–µ (x & y) –±—É–¥–µ—Ç –∏—Å—Ç–∏–Ω–Ω—ã–º —Ç–æ–≥–¥–∞ –∏ —Ç–æ–ª—å–∫–æ —Ç–æ–≥–¥–∞, –∫–æ–≥–¥–∞ x –∏ y –∏—Å—Ç–∏–Ω–Ω—ã. 
–ï—Å–ª–∏ –≤—ã—Ä–∞–∂–µ–Ω–∏–µ –Ω–µ –º–æ–∂–µ—Ç –±—ã—Ç—å –∏—Å—Ç–∏–Ω–Ω—ã–º –Ω–∏ –ø—Ä–∏ –∫–∞–∫–∏—Ö –∑–Ω–∞—á–µ–Ω–∏—è—Ö –ø–µ—Ä–µ–º–µ–Ω–Ω—ã—Ö, —Ç–æ —Ñ—É–Ω–∫—Ü–∏—è –≤–µ—Ä–Ω–µ—Ç —Ä–µ–∑—É–ª—å—Ç–∞—Ç False.'''''
sym.satisfiable(x & ~x) # —Ä–µ–∑—É–ª—å—Ç–∞—Ç False

''''–õ–∏–Ω–µ–π–Ω–∞—è –∞–ª–≥–µ–±—Ä–∞
–ú–∞—Ç—Ä–∏—Ü—ã
–ú–∞—Ç—Ä–∏—Ü—ã –≤ SymPy —Å–æ–∑–¥–∞—é—Ç—Å—è –∫–∞–∫ —ç–∫–∑–µ–º–ø–ª—è—Ä—ã –∫–ª–∞—Å—Å–∞ Matrix:'''''
sym.Matrix([[1, 0], [0, 1]])
#[10] [01]

#–í –æ—Ç–ª–∏—á–∏–µ –æ—Ç NumPy, –º—ã –º–æ–∂–µ–º –∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å –≤ –º–∞—Ç—Ä–∏—Ü–∞—Ö —Å–∏–º–≤–æ–ª—å–Ω—ã–µ –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ:
x, y = sym.symbols('x, y')
A = sym.Matrix([[1, x], [y, 1]])
#A
#[1 ùë¶] [ùë• 1]

#–ò –ø—Ä–æ–∏–∑–≤–æ–¥–∏—Ç—å —Å –Ω–∏–º–∏ —Ä–∞–∑–Ω—ã–µ –º–∞–Ω–∏–ø—É–ª—è—Ü–∏–∏:
A**2
#[ùë•ùë¶+1 2ùë¶] [2ùë• ùë•ùë¶+1]

''''–î–∏—Ñ—Ñ–µ—Ä–µ–Ω—Ü–∏–∞–ª—å–Ω—ã–µ —É—Ä–∞–≤–Ω–µ–Ω–∏—è
–ü—Ä–∏ –ø–æ–º–æ—â–∏ –±–∏–±–ª–∏–æ—Ç–µ–∫–∏ SymPy –º–æ–∂–Ω–æ —Ä–µ—à–∞—Ç—å –Ω–µ–∫–æ—Ç–æ—Ä—ã–µ –æ–±—ã–∫–Ω–æ–≤–µ–Ω–Ω—ã–µ –¥–∏—Ñ—Ñ–µ—Ä–µ–Ω—Ü–∏–∞–ª—å–Ω—ã–µ —É—Ä–∞–≤–Ω–µ–Ω–∏—è. –î–ª—è —ç—Ç–æ–≥–æ –∏—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è 
—Ñ—É–Ω–∫—Ü–∏—è dsolve(). –î–ª—è –Ω–∞—á–∞–ª–∞ –Ω–∞–º –Ω–∞–¥–æ –∑–∞–¥–∞—Ç—å –Ω–µ–æ–ø—Ä–µ–¥–µ–ª–µ–Ω–Ω—É—é —Ñ—É–Ω–∫—Ü–∏—é. –≠—Ç–æ –º–æ–∂–Ω–æ —Å–¥–µ–ª–∞—Ç—å, –ø–µ—Ä–µ–¥–∞–≤ –ø–∞—Ä–∞–º–µ—Ç—Ä cls=Function 
–≤ —Ñ—É–Ω–∫—Ü–∏—é symbols().'''''
f, g = sym.symbols('f g', cls=sym.Function)

#–¢–µ–ø–µ—Ä—å f –∏ g –∑–∞–¥–∞–Ω—ã –∫–∞–∫ –Ω–µ–æ–ø—Ä–µ–¥–µ–ª–µ–Ω–Ω—ã–µ —Ñ—É–Ω–∫—Ü–∏–∏. –º—ã –º–æ–∂–µ–º –≤ —ç—Ç–æ–º —É–±–µ–¥–∏—Ç—å—Å—è, –ø—Ä–æ—Å—Ç–æ –≤—ã–∑–≤–∞–≤ f(x).
f(x) # —Ä–µ–∑—É–ª—å—Ç–∞—Ç ùëì(ùë•)
f(x).diff(x, x) + f(x)
#ùëì(ùë•)+ùëë2/ùëëùë•2ùëì(ùë•)

#–¢–µ–ø–µ—Ä—å —Ä–µ—à–∏–º —Å–ª–µ–¥—É—é—â–µ–µ –¥–∏—Ñ—Ñ–µ—Ä–µ–Ω—Ü–∏–∞–ª—å–Ω–æ–µ —É—Ä–∞–≤–Ω–µ–Ω–∏–µ:
sym.dsolve(f(x).diff(x, x) + f(x), f(x))
# —Ä–µ–∑—É–ª—å—Ç–∞—Ç ùëì(ùë•)=ùê∂1sin(ùë•)+ùê∂2cos(ùë•)
''''–ß—Ç–æ–±—ã —É–ª—É—á—à–∏—Ç—å —Ä–µ—à–∞–µ–º–æ—Å—Ç—å –∏ –ø–æ–º–æ—á—å —ç—Ç–æ–π —Ñ—É–Ω–∫—Ü–∏–∏ –≤ –ø–æ–∏—Å–∫–µ —Ä–µ—à–µ–Ω–∏—è, –º–æ–∂–Ω–æ –ø–µ—Ä–µ–¥–∞–≤–∞—Ç—å –≤ –Ω–µ–µ –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–Ω—ã–µ –∫–ª—é—á–µ–≤—ã–µ –∞—Ä–≥—É–º–µ–Ω—Ç—ã. 
–ù–∞–ø—Ä–∏–º–µ—Ä, –µ—Å–ª–∏ –º—ã –≤–∏–¥–∏–º, —á—Ç–æ —ç—Ç–æ —É—Ä–∞–≤–Ω–µ–Ω–∏–µ —Å —Ä–∞–∑–¥–µ–ª—è–µ–º—ã–º–∏ –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–º–∏, —Ç–æ –º—ã –º–æ–∂–µ–º –ø–µ—Ä–µ–¥–∞—Ç—å –≤ —Ñ—É–Ω–∫—Ü–∏—é –∞—Ä–≥—É–º–µ–Ω—Ç hint='separable'.'''''
sym.dsolve(sym.sin(x) * sym.cos(f(x)) + sym.cos(x) * sym.sin(f(x)) * f(x).diff(x), f(x), hint='separable')
# —Ä–µ–∑—É–ª—å—Ç–∞—Ç [Eq(f(x), -acos(C1/cos(x)) + 2*pi), Eq(f(x),
#            acos(C1/cos(x)))]